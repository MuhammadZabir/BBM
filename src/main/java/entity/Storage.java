package entity;

public class Storage {
    private String hexaOri;
    private String binaryOri;
    private String cipherOri;
    private String binaryCipher;
    private String cipherModify;
    private String binaryModify;

    public String getHexaOri() {
        return hexaOri;
    }

    public void setHexaOri(String hexaOri) {
        this.hexaOri = hexaOri;
    }

    public String getBinaryOri() {
        return binaryOri;
    }

    public void setBinaryOri(String binaryOri) {
        this.binaryOri = binaryOri;
    }

    public String getCipherOri() {
        return cipherOri;
    }

    public void setCipherOri(String cipherOri) {
        this.cipherOri = cipherOri;
    }

    public String getBinaryCipher() {
        return binaryCipher;
    }

    public void setBinaryCipher(String binaryCipher) {
        this.binaryCipher = binaryCipher;
    }

    public String getCipherModify() {
        return cipherModify;
    }

    public void setCipherModify(String cipherModify) {
        this.cipherModify = cipherModify;
    }

    public String getBinaryModify() {
        return binaryModify;
    }

    public void setBinaryModify(String binaryModify) {
        this.binaryModify = binaryModify;
    }
}
